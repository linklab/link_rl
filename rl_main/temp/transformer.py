class EncoderLayer(nn.Module):
    def __init__(self):
        super(EncoderLayer, self).__init__()
        self.layer_norm = nn.LayerNorm(MODEL_DIM, eps=1e-6)
        self.self_attention = MultiHeadAttention()
        self.position_wise_ffn = PositionWiseFeedForward()

    def forward(self, source, source_mask):
        # source          = [batch size, source length, model dim]
        # source_mask     = [batch size, source length, source length]

        # Original Implementation: LayerNorm(x + SubLayer(x))
        # -> Updated Implementation: x + SubLayer(LayerNorm(x))
        normalized_source = self.layer_norm(source)
        # normalized_source = [batch size, source length, model dim]

        # Residual Connection
        output = source + self.self_attention(normalized_source, normalized_source, normalized_source, source_mask)[0]
        # output = [batch size, source length, model dim]

        normalized_output = self.layer_norm(output)

        # Residual Connection
        output = output + self.position_wise_ffn(normalized_output)
        # output = [batch size, source length, model dim]

        return output


class PositionWiseFeedForward(nn.Module):
    def __init__(self):
        super(PositionWiseFeedForward, self).__init__()
        # nn.Conv1d takes input whose size is (N, C): N is a batch size, C denotes a number of channels
        self.conv1 = nn.Conv1d(
            MODEL_DIM, FEED_FORWARD_DIM, kernel_size=1
        )
        self.conv2 = nn.Conv1d(
            FEED_FORWARD_DIM, MODEL_DIM, kernel_size=1
        )
        self.init_weight(self.conv1)
        self.init_weight(self.conv2)
        self.dropout = nn.Dropout(DROPOUT)

    @staticmethod
    def init_weight(layer):
        nn.init.xavier_uniform_(layer.weight)
        if layer.bias is not None:
            nn.init.constant_(layer.bias, 0)

    def forward(self, x):
        # x = [batch size, sentence length, model dim]

        # permute x's indices to apply nn.Conv1d on input 'x'
        x = x.permute(0, 2, 1)  # x = [batch size, model dim, sentence length]
        output = self.dropout(F.relu(self.conv1(x)))  # output = [batch size, feed forward dim, sentence length)
        output = self.conv2(output)  # output = [batch size, model dim, sentence length)

        # permute again to restore output's original indices
        output = output.permute(0, 2, 1)  # output = [batch size, sentence length, model dim]
        return self.dropout(output)


class Encoder(nn.Module):
    def __init__(self):
        super(Encoder, self).__init__()
        self.token_embedding = nn.Embedding(
            num_embeddings=INPUT_DIM, embedding_dim=MODEL_DIM, padding_idx=PAD_IDX
        )
        nn.init.normal_(
            self.token_embedding.weight, mean=0, std=MODEL_DIM ** -0.5
        )
        self.embedding_scale = MODEL_DIM ** 0.5
        self.pos_embedding = nn.Embedding.from_pretrained(
            create_positional_encoding(MAX_LEN + 1, MODEL_DIM), freeze=True
        )
        self.encoder_layers = nn.ModuleList(
            [EncoderLayer() for _ in range(N_LAYER)]
        )
        self.dropout = nn.Dropout(DROPOUT)
        self.layer_norm = nn.LayerNorm(MODEL_DIM, eps=1e-6)

    def forward(self, source):
        # source = [batch size, source length]
        source_mask = create_source_mask(source)  # [batch size, source length, source length]

        source_pos = create_position_vector(source)  # [batch size, source length]
        source = self.token_embedding(source) * self.embedding_scale
        source = self.dropout(source + self.pos_embedding(source_pos))
        # source = [batch size, source length, model dim]

        for encoder_layer in self.encoder_layers:
            source = encoder_layer(source, source_mask)
        # source = [batch size, source length, model dim]

        return self.layer_norm(source)


"""# Decoder 구성"""


class DecoderLayer(nn.Module):
    def __init__(self):
        super(DecoderLayer, self).__init__()
        self.layer_norm = nn.LayerNorm(MODEL_DIM, eps=1e-6)
        self.self_attention = MultiHeadAttention()
        self.encoder_attention = MultiHeadAttention()
        self.position_wise_ffn = PositionWiseFeedForward()

    def forward(self, target, encoder_output, target_mask, encoder_decoder_mask):
        # target          = [batch size, target length, model dim]
        # encoder_output  = [batch size, source length, model dim]
        # target_mask     = [batch size, target length, target length]
        # encoder_decoder_mask    = [batch size, target length, source length]

        # Original Implementation: LayerNorm(x + SubLayer(x)) -> Updated Implementation: x + SubLayer(LayerNorm(x))
        norm_target = self.layer_norm(target)
        output = target + self.self_attention(norm_target, norm_target, norm_target, target_mask)[0]

        # In Decoder stack, query is the output from below layer and key & value are the output from the Encoder
        norm_output = self.layer_norm(output)
        sub_layer, encoder_decoder_attention_map = self.encoder_attention(norm_output, encoder_output, encoder_output, encoder_decoder_mask)
        output = output + sub_layer

        norm_output = self.layer_norm(output)
        output = output + self.position_wise_ffn(norm_output)
        # output = [batch size, target length, model dim]

        return output, encoder_decoder_attention_map


class Decoder(nn.Module):
    def __init__(self):
        super(Decoder, self).__init__()
        self.token_embedding = nn.Embedding(
            num_embeddings=OUTPUT_DIM, embedding_dim=MODEL_DIM, padding_idx=PAD_IDX
        )
        nn.init.normal_(
            self.token_embedding.weight, mean=0, std=MODEL_DIM ** -0.5
        )
        self.embedding_scale = MODEL_DIM ** 0.5
        self.pos_embedding = nn.Embedding.from_pretrained(
            create_positional_encoding(MAX_LEN + 1, MODEL_DIM), freeze=True
        )
        self.decoder_layers = nn.ModuleList(
            [DecoderLayer() for _ in range(N_LAYER)]
        )
        self.dropout = nn.Dropout(DROPOUT)
        self.layer_norm = nn.LayerNorm(MODEL_DIM, eps=1e-6)

    def forward(self, target, source, encoder_output):
        # target              = [batch size, target length]
        # source              = [batch size, source length]
        # encoder_output      = [batch size, source length, model dim]

        target_mask, encoder_decoder_mask = create_target_mask(source, target)
        # target_mask           = [batch size, target length, target length]
        # encoder_decoder_mask = [batch size, target length, source length]

        target_pos = create_position_vector(target)  # target_pos = [batch size, target length]
        target = self.token_embedding(target) * self.embedding_scale
        target = self.dropout(target + self.pos_embedding(target_pos))
        # target = [batch size, target length, model dim]

        for decoder_layer in self.decoder_layers:
            target, encoder_decoder_attention_map = decoder_layer(
                target, encoder_output, target_mask, encoder_decoder_mask
            )
        # target = [batch size, target length, model dim]

        target = self.layer_norm(target)
        output = torch.matmul(target, self.token_embedding.weight.transpose(0, 1))
        # output = [batch size, target length, output dim]
        return output, encoder_decoder_attention_map


"""# Transformer 구성"""


class Transformer(nn.Module):
    def __init__(self):
        super(Transformer, self).__init__()
        self.encoder = Encoder()
        self.decoder = Decoder()

    def forward(self, source, target):
        # source = [batch size, source length]
        # target = [batch size, target length]

        # [batch size, source length, model dim]
        encoder_output = self.encoder(source)

        # [batch size, target length, output dim]
        output, encoder_decoder_attention_map = self.decoder(target, source, encoder_output)

        return output, encoder_decoder_attention_map

    def count_params(self):
        return sum(p.numel() for p in self.parameters() if p.requires_grad)


class ScheduledAdam():
    def __init__(self, optimizer, model_dim, warm_steps):
        self.init_lr = np.power(model_dim, -0.5)
        self.optimizer = optimizer
        self.current_steps = 0
        self.warm_steps = warm_steps

    def step(self):
        # Update learning rate using current step information
        self.current_steps += 1
        lr = self.init_lr * self.get_scale()

        for p in self.optimizer.param_groups:
            p['lr'] = lr

        self.optimizer.step()

    def zero_grad(self):
        self.optimizer.zero_grad()

    def get_scale(self):
        return np.min([
            np.power(self.current_steps, -0.5),
            self.current_steps * np.power(self.warm_steps, -0.5)
        ])


class Trainer:
    def __init__(self):
        self.model = Transformer()
        self.model.to(device)

        # Scheduling Optimzer
        self.optimizer = ScheduledAdam(
            torch.optim.Adam(
                self.model.parameters(),
                betas=(0.9, 0.98),
                eps=1e-9
            ),
            model_dim=MODEL_DIM,
            warm_steps=WARM_STEPS
        )

        self.criterion = nn.CrossEntropyLoss(ignore_index=PAD_IDX)
        self.criterion.to(device)

    def train(self):
        print(self.model)
        print(f'The model has {self.model.count_params():,} trainable parameters')
        best_valid_loss = float('inf')

        for epoch in range(NUM_EPOCH):
            self.model.train()
            epoch_loss = 0
            start_time = time.time()

            for batch in train_iter:
                # For each batch, first zero the gradients
                self.optimizer.zero_grad()
                source = batch.kor      # source = [batch_size, 9],  [batch_size, 8]
                target = batch.eng      # target = [batch_size, 27], [batch_size, 21]

                # target sentence consists of <sos> and following tokens (except the <eos> token)
                output, encoder_decoder_attention_map = self.model(source, target[:, :-1])

                # ground truth sentence consists of tokens and <eos> token (except the <sos> token)
                # contiguous: it will change the data in memory so that the map from indices to memory location is the canonical one
                output = output.contiguous().view(-1, output.shape[-1])
                target = target[:, 1:].contiguous().view(-1)

                # output = [(batch size * target length - 1), output dim]
                # target = [(batch size * target length - 1)]
                loss = self.criterion(output, target)
                loss.backward()

                # clip the gradients to prevent the model from exploding gradient
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), CLIP)

                self.optimizer.step()

                # 'item' method is used to extract a scalar from a tensor which only contains a single value.
                epoch_loss += loss.item()

            train_loss = epoch_loss / len(train_iter)
            valid_loss = self.evaluate()

            end_time = time.time()
            epoch_mins, epoch_secs = epoch_time(start_time, end_time)

            if valid_loss < best_valid_loss:
                best_valid_loss = valid_loss
                torch.save(self.model.state_dict(), SAVE_MODE)

            print(f'Epoch: {epoch + 1:02} | Epoch Time: {epoch_mins}m {epoch_secs}s')
            print(f'\tTrain Loss: {train_loss:.3f} | Val. Loss: {valid_loss:.3f}')

    def evaluate(self):
        self.model.eval()
        epoch_loss = 0

        with torch.no_grad():
            for batch in valid_iter:
                source = batch.kor
                target = batch.eng

                output = self.model(source, target[:, :-1])[0]

                output = output.contiguous().view(-1, output.shape[-1])
                target = target[:, 1:].contiguous().view(-1)

                loss = self.criterion(output, target)

                epoch_loss += loss.item()

        return epoch_loss / len(valid_iter)


def epoch_time(start_time, end_time):
    """
    Calculate the time spent to train one epoch
    Args:
        start_time: (float) training start time
        end_time: (float) training end time
    Returns:
        (int) elapsed_mins and elapsed_sec spent for one epoch
    """
    elapsed_time = end_time - start_time
    elapsed_mins = int(elapsed_time / 60)
    elapsed_secs = int(elapsed_time - (elapsed_mins * 60))

    return elapsed_mins, elapsed_secs


