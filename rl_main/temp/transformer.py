class SelfAttention(nn.Module):
    def __init__(self):
        super(SelfAttention, self).__init__()
        self.model_dim = MODEL_DIM
        self.attention_dim = MODEL_DIM // N_HEAD

        self.q_w = nn.Linear(self.model_dim, self.attention_dim, bias=False)
        self.k_w = nn.Linear(self.model_dim, self.attention_dim, bias=False)
        self.v_w = nn.Linear(self.model_dim, self.attention_dim, bias=False)
        self.init_weight(self.q_w)
        self.init_weight(self.k_w)
        self.init_weight(self.v_w)

        self.dropout = nn.Dropout(DROPOUT)
        self.scale_factor = torch.sqrt(
            torch.FloatTensor([self.attention_dim])
        ).to(device)

    @staticmethod
    def init_weight(layer):
        nn.init.xavier_uniform_(layer.weight)
        if layer.bias is not None:
            nn.init.constant_(layer.bias, 0)

    def forward(self, query, key, value, mask=None):
        # query, key, value = [batch size, sentence length, model dim]

        # create Q, K, V matrices using identical input sentence to calculate self-attention episode_reward
        q = self.q_w(query)
        k = self.k_w(key)
        v = self.v_w(value)
        # q, k, v = [batch size, sentence length, attention dim]

        # Performs a batch matrix-matrix product of matrices stored in input and mat.
        self_attention = torch.bmm(q, k.permute(0, 2, 1))
        self_attention = self_attention / self.scale_factor
        # self_attention = [batch size, sentence length, sentence length]

        if mask is not None:
            self_attention = self_attention.masked_fill(mask, -np.inf)

        # normalize self attention episode_reward by applying soft max function on each row
        attention_score = F.softmax(self_attention, dim=-1)
        norm_attention_score = self.dropout(attention_score)
        # attention_score = [batch size, sentence length, sentence length]

        # compute "weighted" value matrix using self attention episode_reward and V matrix
        weighted_v = torch.bmm(norm_attention_score, v)
        # weighted_v = [batch size, sentence length, attention dim]

        return self.dropout(weighted_v), attention_score


class MultiHeadAttention(nn.Module):
    def __init__(self):
        super(MultiHeadAttention, self).__init__()

        self.attentions = nn.ModuleList(
            [SelfAttention() for _ in range(N_HEAD)]
        )
        self.o_w = nn.Linear(MODEL_DIM, MODEL_DIM, bias=False)
        self.init_weight(self.o_w)
        self.dropout = nn.Dropout(DROPOUT)

    @staticmethod
    def init_weight(layer):
        nn.init.xavier_uniform_(layer.weight)
        if layer.bias is not None:
            nn.init.constant_(layer.bias, 0)

    def forward(self, query, key, value, mask=None):
        # query, key, value = [batch size, sentence length, model dim]

        self_attentions = [
            attention(query, key, value, mask) for attention in self.attentions
        ]
        # self_attentions = [batch size, sentence length, attention dim] * num head
        weighted_vs = [weighted_v[0] for weighted_v in self_attentions]
        attentions = [weighted_v[1] for weighted_v in self_attentions]

        weighted_v = torch.cat(weighted_vs, dim=-1)
        # weighted_v = [batch size, sentence length, model dim]

        output = self.dropout(self.o_w(weighted_v))
        # output = [batch size, sentence length, model dim]

        return output, attentions


