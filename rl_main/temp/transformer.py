# Cohesion episode_reward + L-Tokenizer. 띄어쓰기가 잘 되어있는 한국어 문서를 위한 unsupervised tokenizer 생성
def build_korean_tokenizer():
    """
    Train soynlp tokenizer which will be used to tokenize Korean input sentence
    """
    print(f'Now building soy-nlp tokenizer . . .')

    train_file = 'https://github.com/Huffon/pytorch-transformer-kor-eng/raw/master/data/corpus.csv'
    df = pd.read_csv(train_file, encoding='utf-8')

    # if encounters non-text row, we should skip it
    kor_lines = [
        row.korean for _, row in df.iterrows() if type(row.korean) == str
    ]

    word_extractor = WordExtractor(min_frequency=5)
    word_extractor.train(kor_lines)

    word_scores = word_extractor.extract()
    cohesion_scores = {
        word: episode_reward.cohesion_forward for word, episode_reward in word_scores.items()
    }

    tokenizer = LTokenizer(scores=cohesion_scores)

    return tokenizer


def clean_text(text):
    """
    텍스트를 정규화하기 위하여 입력된 text로 부터 스페셜 문자 제거
    Args:
        text: (string) text string which may contain special character
    Returns:
        normalized sentence
    """
    text = re.sub('[-=+,#/\?:^$.@*\"※~&%ㆍ!』\\‘|\(\)\[\]\<\>`…》]', '', text)
    return text


def convert_to_dataset(data, kor_field, eng_field):
    """
    Pre-process input DataFrame and convert pandas DataFrame to torchtext Dataset.
    Args:
        data: (DataFrame) pandas DataFrame to be converted into torchtext Dataset
        kor_field: torchtext Field containing Korean sentence
        eng_field: torchtext Field containing English sentence
    Returns:
        (Dataset) torchtext Dataset containing 'kor' and 'eng' Fields
    """
    # drop missing values not containing str value from DataFrame
    missing_rows = [idx for idx, row in data.iterrows()
                    if type(row.korean) != str or type(row.english) != str]

    data = data.drop(missing_rows)

    # convert each row of DataFrame to torchtext 'Example' containing 'kor' and 'eng' Fields
    list_of_examples = [
        torchtext.data.Example.fromlist(
            row.apply(lambda x: clean_text(x)).tolist(),
            fields=[('kor', kor_field), ('eng', eng_field)]
        ) for _, row in data.iterrows()
    ]

    # construct torchtext 'Dataset' using torchtext 'Example' list
    dataset = torchtext.data.Dataset(
        examples=list_of_examples,
        fields=[('kor', kor_field), ('eng', eng_field)]
    )

    return dataset


